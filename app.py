@app.route("/download-report")
def download_report():

    pdf_file = "resume_report.pdf"

    c = canvas.Canvas(pdf_file)

    c.drawString(100, 800, "Resume Analysis Report")
    c.drawString(100, 770, "Generated by Resume Analyzer")

    c.save()

    return send_file(
        pdf_file,
        as_attachment=True
    )

if __name__ == "__main__":
    app.run(debug=True)